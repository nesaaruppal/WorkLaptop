##### WHERE TO APPLY #####

# color: blue
# Property: Value


##### ELEMENT SELECTOR #####
# CSS Selector --> E.G. Target "h1"
    # h1{
        #color:blue
    #}

# Selects a particular HTML tag and applies whichever rules have been coded in CSS
# If it's "h2" or "p" then it becomes an ALL ELEMENT SELECTOR and will apply to multiple tags


##### CLASS SELECTOR ######
# .red-heading {
    #color: red
#}
    #red-heading is the CLASS

# Can add a class to ANY HTML element


##### ID SELECTOR #####
#   #main{
     #color:red
#}

##### ATTRIBUTE SELECTOR #####
    #"p" --> Attribute you want to select
    # ALL "p" elements WITH the attribute "draggable"
# p[draggable]{
    #color:red
#}

    # OR
    # p[draggable="false"]{
        #color:red;
    #}
    
##### UNIVERSAL SELECTOR #####
    #* {
    #    color:red;
    #}