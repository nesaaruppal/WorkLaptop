# Using a basic FOR Loop
fruits = ["Apple", "Orange", "Pear"]
for fruit in fruits:
    print(fruit)
    print(fruit + " Pie")
    print(fruits)

print(fruits)


# With the RANGE function
for number in range(1, 10):
    print(number)

for number in range(1, 11):
    print(number)

for number in range(1, 11, 3):
    print(number)
